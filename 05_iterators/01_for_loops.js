// for loop

for (let index = 0; index <= 10; index++) {
    console.log(`Index: ${index}`);   
}


let myArray = ["ironman", "thor", "hulk", "captainAmerica", "blackWidow", "hawkeye"];

for (let index = 0; index < myArray.length; index++) {
    console.log(`Index: ${index}, Value: ${myArray[index]}`);
}

// remember, if you want to stop the loop or you need to make an index not used, you can use break or continue